/****************************
 * 寻找数组内偶数的个数
 * 输入 : 数组首地址p
 * 输入 : 数组长度nArraySize
 * 输出 : 偶数的个数n
 ****************************/
void FindEvens(int* p, int nArraySize, int* n) 
{ 
	int *x;                            //定义指针变量
	x = &p[0];                         //赋值
	for (int i = 0; i < 5; i++) {
		if (*(x+ i) % 2 == 0)
			*n += 1;                   //计数
	}
}