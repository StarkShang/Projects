/***************************************
 * 求出x的偶数因子
 * 并按从小到大的顺序放在pp所指的数组中
 * 输入 : 整数x
 * 输出 : 偶数因子数组pp
 * 输出 : 因子个数n
 ***************************************/
void func__(int x, int* pp, int& n)
{
	n = 0;                            //初始化变量
	int j=0;                          //初始化变量
	for (int i = 1; i <= x; i++) {   
		if (x%i == 0&&i%2==0) {
			n += 1;                   //计数
			pp[j] = i;                //赋值
			j += 1;
		}
	}
	
}