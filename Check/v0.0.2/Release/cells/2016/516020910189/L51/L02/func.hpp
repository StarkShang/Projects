/***************************************
 * 求出x的偶数因子
 * 并按从小到大的顺序放在pp所指的数组中
 * 输入 : 整数x
 * 输出 : 偶数因子数组pp
 * 输出 : 因子个数n
 ***************************************/
void func19(int x, int* pp, int& n)
{
	n = 0;
	for (int i = 2; i < x; i = i + 2)
	{
		if (x % i == 0)       //判断为偶数因子
		{
			*(pp + n) = i;    //将偶数因子从小到大放在pp所指的数组中
			n = n + 1;        //因子个数加一
		}
	}
}